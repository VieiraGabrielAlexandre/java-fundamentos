package fundamentos;

import java.util.Arrays;

public class Inferencia {
    public static void main(String[] args) {
        double a = 4.5;
        System.out.println("a = " + a);

        a = 12.5;
        System.out.println("a = " + a);

        var b = 4.85;
        System.out.println("b = " + b);

        var c = "TEXTO";

        System.out.println("c = " + c);

        c = "OUTRO TEXTO";

        System.out.println("c = " + c);
    }
}
